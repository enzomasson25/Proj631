public class donnee {

	private int id;
	private int taille; 
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getTaille() {
		return taille;
	}
	public void setTaille(int taille) {
		this.taille = taille;
	}
	
	public donnee(int id, int taille) {
		super();
		this.id = id;
		this.taille = taille;
	}
	
	

}
